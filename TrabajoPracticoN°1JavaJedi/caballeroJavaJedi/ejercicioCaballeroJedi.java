package caballeroJavaJedi;

public class ejercicioCaballeroJedi {
    public static void main(String[] args) {
        /* Obtener porcentaje : Dados dos números, uno real y el otro entero. Obtenga el
        porcentaje que indique el segundo número del primer número. */
        int numero1 = 80;
        double numero2 = 40.5;
        double porcentaje = (numero2 / numero1) * 100;
        System.out.println("El porcentaje que indica el segundo número del primer número es %" + porcentaje);

    }
}
